use cbb108018;

drop table if exists Movies;
drop table if exists MovieStar;
drop table if exists StarsIn;
drop table if exists MovieExec;
drop table if exists Studio;

create table Movies
(
  title char(30),
  year integer,
  length integer,
  genre char(20),
  studioName char(20),
  producerCID integer,
  primary key (title,year)
);

create table MovieStar
(
  name char(30),
  address char(50),
  gender char(1),
  birthdate DATE,
  primary key (name)
);

create table StarsIn
(
  movieTitle char(30),
  movieYear integer,
  starName char(30),
  primary key (movieTitle, movieYear, starName)
);

create table MovieExec
(
  name char(30),
  address char(50),
  certID integer,
  netWorth integer,
  primary key (certID)
);

create table Studio
(
  name char(30),
  address char(50),
  presCID integer,
  primary key (name)
);

